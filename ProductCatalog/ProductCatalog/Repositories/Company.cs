                Console.WriteLine(" =======================================================================================================================================================");
                foreach (var r in res)
                {
                    Console.WriteLine(r);
                    Console.WriteLine(" -------------------------------------------------------------------------------------------------------------------------------------------------------");
                }
                res = Employees.Where(x => x.SecurityLvl == 3);
                Console.WriteLine("\n\n\n =======================================================================================================================================================");
                Console.WriteLine(" |                                                                      Managers list                                                                  |");
                Console.WriteLine(" =======================================================================================================================================================");
                Console.WriteLine($" | {"Name",10} | {"SurName",10} | {"Age",10} | {"Position",33} | {"PhoneNumber",15} | {"Salary",10} | {"Login",10} | {"Password",10} | {"Security Level",15} |");
                Console.WriteLine(" =======================================================================================================================================================");
